    private void panel_buscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panel_buscarMouseClicked
       try {
        Conexion con = new Conexion();
        con.ConexionPostgres();
        
        String query ="SELECT * FROM familia WHERE id_familia = "+ Integer.parseInt(ingresar_id.getText().trim());
        
        ResultSet rs = con.consultar(query);
        
        if(rs.next()){
            ingresar_nombre.setText(rs.getString("nombre_familia"));
            ingresar_descripcion.setText(rs.getString("descripcion_familia"));
        }
        else{
            JOptionPane.showMessageDialog(this,"No existe la familia");
        }
        ingresar_id.setForeground(Color.black);
        ingresar_nombre.setForeground(Color.black);
        ingresar_descripcion.setForeground(Color.black);
        
         con.cerrar();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Familia.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Familia.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InstantiationException ex) {
            Logger.getLogger(Familia.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IllegalAccessException ex) {
            Logger.getLogger(Familia.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_panel_buscarMouseClicked

